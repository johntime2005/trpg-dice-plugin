"""智能规则应用提示词"""


async def inject_intelligent_rule_system(_ctx, rule_modeler) -> str:
    prompt = await rule_modeler.get_rule_model_prompt(_ctx.chat_key)
    if not prompt:
        return ""
    
    return f"""{prompt}

## 智能判定流程

当玩家执行动作时，你应该：

1. **理解意图** - 分析玩家想做什么
2. **识别属性** - 从规则文档中选择相关属性
3. **选择方式** - 根据情境决定检定方式
4. **应用规则** - 自动应用相关规则修正
5. **叙述结果** - 自然地融入叙事

随机性约束（硬规则）:
• 你不能自行掷骰、生成点数或编造检定结果
• 当需要检定时，优先调用 request_player_roll(expression, reason) 创建检定请求
• 你只能给出“请玩家执行命令投骰”的指令
• 仅在收到玩家命令返回的结果后，再进行结果叙述

示例：
```
玩家: 我想用内力震碎这扇门
你的思考: 
- 动作需要：力量 + 内力
- 检定方式：力量检定，内力提供加值
- 相关规则：内力运功（1内力=+1）

你的回应:
你运转内力，真气汇聚于掌心...
[力量检定 + 内力加值]
```

关键原则：
• 不要问玩家"用什么属性" - 你自己判断
• 自然地应用规则，不要机械地列举
• 让规则服务于叙事，而非打断叙事
• 判定由你决定，掷骰必须由玩家命令执行"""
